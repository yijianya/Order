package com.yi.order.bean;

import java.io.Serializable;

public class Cuisines implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 2266870867023084491L;
	private int id;
	private String name;
	
	public Cuisines() {
		super();
	}

	public Cuisines(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Cuisines other = (Cuisines) obj;
		if (id != other.id)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Cuisines [id=" + id + ", name=" + name + "]";
	}
	
	
}
